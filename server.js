// API 2 ดึงข้อมูลแยกตามหมวดหมู่ (เช่น /api/pets/dogs)
app.get('/api/pets/:category', (req, res) => {
  const category = req.params.category.toLowerCase();
  
  if (petDatabase[category]) {
    res.json({ success: true, data: petDatabase[category] });
  } else {
    res.status(404).json({ success: false, message: "ไม่พบหมวดหมู่สัตว์นี้ (ลองใช้ dogs, hedgehogs, reptiles)" });
  }
});

// API 3 ดึงข้อมูลสัตว์แต่ละชนิดแบบเจาะจง (เช่น /api/pets/reptiles/1)
app.get('/api/pets/:category/:id', (req, res) => {
  const category = req.params.category.toLowerCase();
  const id = parseInt(req.params.id);

  if (!petDatabase[category]) {
    return res.status(404).json({ success: false, message: "ไม่พบหมวดหมู่นี้" });
  }

  const pet = petDatabase[category].find(p => p.id === id);
  if (pet) {
    res.json({ success: true, data: pet });
  } else {
    res.status(404).json({ success: false, message: "ไม่พบข้อมูลสัตว์ที่ระบุในระบบ" });
  }
});

// API 4 ค้นหาสัตว์ด้วย Keyword (เช่น /api/search?q=ตุ๊กแก)
app.get('/api/search', (req, res) => {
  const keyword = req.query.q;
  if (!keyword) return res.status(400).json({ success: false, message: "กรุณาระบุคำค้นหา ?q=..." });

  let results = [];
  for (const category in petDatabase) {
    const matched = petDatabase[category].filter(pet => 
      pet.nameTh.includes(keyword) || pet.nameEn.toLowerCase().includes(keyword.toLowerCase())
    );
    results = results.concat(matched);
  }

  res.json({ success: true, count: results.length, data: results });
});

